export function normalizeTeam(value) {
  return String(value ?? '')
    .replace(/T\S*rkiye/gi, 'Turkiye')
    .replace(/Cura\S*ao/gi, 'Curacao')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9 ]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

export function canonicalTeam(value) {
  const normalized = normalizeTeam(value)
  return TEAM_NAME_ALIASES[normalized] ?? String(value ?? '').trim()
}
